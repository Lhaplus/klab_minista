<p>Minista.jpの原点は2013年3月9日(土) 午前7時31分に誕生した、とあるアイデアがきっかけです。<br>
それは、「世の中の最低限を考え、集約し、共有する」というものです。<br>
あらゆる情報が爆発的に増加・発信・共有されるこの現代社会において、「いたってシンプルに「情報」と接することができるべき」というコンセプトにもとづいています。<br>
何か行動を起こす前や、ふと何かに興味を持った時、そして人生の節目に、その指針を簡潔に示してくれるサイト、その名もミニマムスタンダードサイト「ミニスタ」の誕生です。<br>
Minista.jpは近い将来、最低限の情報で最大限の影響を与えるサービスとなることでしょう。</p>

