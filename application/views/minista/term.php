<p>我々は、本サービスに起因して利用者に生じたあらゆる損害について一切の責任を負いかねます。

<h4>第8条　退会、利用停止等</h4>
<p>1.本サービスに会員登録した利用者は、いつでもアカウントを削除して退会することができます。この場合、我々が保有する利用者ご本人に関する情報は、個人情報保護法その他の関連法規およびプライバシーポリシーにしたがい処理されます。なお、第4条第2項にもとづき我々が取得した公開投稿コンテンツの利用権は、利用者の退会後も有効に存続します。<br>
2.我々は、利用者が本規約に違反し、または違反するおそれがあると認めた場合、あらかじめ利用者に通知することなく、アカウントを停止または廃止することができます。<br>
3.他の事業者が提供するアカウント（以下「他社アカウント」）で本サービスにログインする利用者は、他社アカウントが削除される等により、他社アカウントがこれを提供する事業者によって認証されなくなった場合、他社アカウントにより本サービスにログインして、本サービスを利用することはできなくなります。</p>

<h4>第9条　通知または連絡の方法</h4>
<p>1.本サービスに関する我々から利用者への通知または連絡は、我々が運営するウェブサイト内の適当な場所への掲示その他、我々が適当と判断する方法により行います。<br>
2.本サービスに関する利用者から我々への通知または連絡は、我々が運営するウェブサイト内の適当な場所に設置するお問い合わせフォームの送信または我々が指定する方法により行っていただきます。</p>

<h4>第10条　本規約等の変更</h4>
<p>我々は、我々が必要と判断する場合、あらかじめ利用者に通知することなく、いつでも本規約を変更できるものとします。変更後の本規約は、我々が運営するウェブサイトの適当な場所に掲示された時点からその効力を生じるものとし、利用者は本規約の変更後も本サービスを使い続けることにより、変更後の本規約に対する有効かつ取消不能な同意をしたものとみなされます。かかる変更の内容を利用者のみなさまの個別に通知することはいたしかねますので、本サービスをご利用の際には、随時、最新の本規約をご参照ください。</p>

<h4>第11条　準拠法</h4>
<p>本規約は日本語を正文とし、その準拠法は日本法とします。</p>

