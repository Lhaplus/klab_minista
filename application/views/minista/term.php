<p>1.利用者は、本サービスを利用するにあたり、必要なパソコン、携帯電話、通信機器、通信手段および電力などを、利用者の費用と責任で用意しなければなりません。<br>
2.我々は、本サービスの提供を受けることができる利用者を、会員登録の有無、年齢、ご本人確認の有無、その他、我々が必要と判断する条件を満たしたお客様に限定することができるものとします。<br>
3.我々は、本サービスに我々または第三者の広告を掲載することができるものとします。<br>
4.我々は、我々が必要と判断する場合、あらかじめ利用者に通知することなく、いつでも、本サービスの全部または一部の内容を変更し、また、その提供を中止することができるものとします。<br>
