<p>利用者は、利用者が法令または本規約に違反して本サービスを利用したことに起因して、我々が直接的もしくは間接的に何らかの損害、損失または費用負担を被った場合、我々の請求にしたがってただちにこれを賠償または補償しなければなりません。</p>

